
'''
Identifiers are names used to identify a variable, function, class, module, or other objects. 

They can contain letters (a-z, A-Z), digits (0-9), and underscores (_). 

They must start with a letter or an underscore but cannot start with a digit. 

Identifiers are case-sensitive.
'''

